# String example
name = "Alice"
print(f"Hello, {name}!")

# List example
fruits = ["apple", "banana", "cherry"]
print(fruits[0])

# Dictionary example
person = {"name": "Bob", "age": 25}
print(person["name"])

# Loop with condition
for num in range(1, 6):
    if num % 2 == 0:
        print(f"{num} is even")
